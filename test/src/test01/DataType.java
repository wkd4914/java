package test01;

public class DataType {
	
	public static void main(String[] args) {
		int a = 2;
		int b = 4;
		
		System.out.println(a==b);
		System.out.println(a!=b);
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
	}
}
